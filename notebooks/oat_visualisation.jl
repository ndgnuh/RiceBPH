function ploterrorbar(x, m, s; name = "", layout=Config())
	xerr = [x; reverse(x)]
	@info x, m, s
	yerr = [m + s; reverse(m) - reverse(s)]
	traces = [
		Config(x = xerr, y = yerr,
			error_y=s,
			showlegend=false,
			fill="tozerox", 
			fillcolor = "rgb(0, 160, 240, 0.02)",
			name=name),
		Config(
			x = x, 
			y = m, 
			name = name,
			line = (color = "rgb(0, 80, 120)",)
		),
	]
	try
		layout.xaxis.range = (minimum(x), maximum(x))
	catch e
	end
	Plot(traces, layout)
end

# ╔═╡ 1ddca6ce-2491-480c-ae42-aae04f10c35d
function fillnan(X, method="mean")
	rep = if method == "mean"
		mean(x for x in X if !isnan(x))
	else
		method
	end
	replace(X, NaN => rep)
end

